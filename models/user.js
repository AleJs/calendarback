const { Schema, model } = require("mongoose");

//se crea el esqueleto de la bd
const UserSchema = Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports = model("user", UserSchema);
